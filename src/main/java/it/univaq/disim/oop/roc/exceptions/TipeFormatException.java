package it.univaq.disim.oop.roc.exceptions;

public class TipeFormatException extends Exception {
	
	public TipeFormatException() {
		super();
	}

	public TipeFormatException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public TipeFormatException(String message, Throwable cause) {
		super(message, cause);
	}

	public TipeFormatException(String message) {
		super(message);
	}

	public TipeFormatException(Throwable cause) {
		super(cause);
	}
}
